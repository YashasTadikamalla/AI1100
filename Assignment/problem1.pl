% density of a country=(population of the country)/(area of the country).

%
% density(countryA,den_A) means countryA has a density of den_A.
%


density(X,Y) :- area(X,Area), population(X,Pop), Y is Pop/Area.
% IF X has an area of Area and population of Pop, THEN X has a density of Pop/Area

%
% big_pop_and_area(countryX,countryY) means countryX has both, greater area and population compared to countryY
%


big_pop_and_area(X,Y) :- neighbour(X,Y), area(X,Area_X), area(Y,Area_Y), Area_X>Area_Y, population(X,Pop_X),population(Y,Pop_Y),Pop_X>Pop_Y.
% IF X,Y are neighbours and Pop_X,Area_X are greater than Pop_Y,Area_Y respectively,
% where Area_X is area of X, Pop_X is population of X,
% Area_Y is area of Y, Pop_Y is population of Y,
% THEN X has big_pop_and_den than Y


%
% higher_den(countryX,countryY) means countryX has higher density than countryY
%


higher_den(X,Y) :- neighbour(X,Y), density(X,Den_X), density(Y,Den_Y), Den_X>Den_Y.
% IF X,Y are neighbours and Den_X is greater than Den_Y, where
% Den_X is density of X, Den_Y is density of Y,
% THEN X has higher_den than Y


